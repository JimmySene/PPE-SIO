<p>
 Pierre Bartholi, l’initiateur du projet, âgé de 45 ans à la création de la société et Marc Bartholi frère du créateur, ont décidé de créer leur société en 2010. 
Ils ont choisi de créer un société sous la forme SARL.
Pierre Bartholi possède 55% des parts sociales à lui seul et son épouse, Martine Bartholi, accompagne son mari et possédera 10% des parts sociales.</p>

<p>Son jeune frère âgé alors de 40 ans rejoint la société et obtient par son apport financier 25% des parts sociales.
C’est tout naturellement que Laurence, épouse de Marc, accompagne également son mari et
prendra possession des 10% de parts sociales restantes.</p>

	<p>Ce sont 100.000 euros au total qui ont permis de composer le capital de la société grâce aux
apports financiers des actionnaires.
Pierre Bartholi souhaite que le nom de cette société soit représentatif de son activité et c'est ainsi que les deux frères et leurs épouses se sont accordés sur « BELLETABLE ».
</p>

<p>Belle Table est une société qui vendra et loura tous les matériels, la vaisselle, les couverts et tous autres accessoires permettant de dresser une « Belle Table ».
</p>
<p>La société a démarré avec seulement 4 salariés : 2 commerciaux, 1 comptable, 1 préparateur de commandes.</p>

	<p>Le chiffre d’affaires de la première année était de 750 K€, avec un bénéfice avant impôts de 90 K€.
Cette première année très encourageante a permis de développer la communication et d’augmenter l’équipe pour passer à 10 salariés.
La deuxième année a connu un vif succès. C’est un chiffre d’affaires de 1,8 M€ qui est acqui. Il permettra un bénéfice avant impôts de 150 K€.
La troisième année d’activité, Pierre est devenu Directeur Général, Marc Directeur Marketing. Les épouses des actionnaires ont intégré la société également,
aux postes de Responsable de magasin pour Martine, Responsable vente à distance et locations pour Laurence.</p>

	<p>L’équipe a été de nouveau renforcée avec 6 embauches supplémentaires au cours de la troisième année afin de faire face à l’augmentation d’activité.
Aujourd’hui, la société continue de prospérer avec une progression à deux chiffres du CA chaque année pour atteindre 3,8 M€ au dernier exercice.
Le nombre de salariés a suivi cette évolution. Ce sont 40 salariés qui travaillent au sein de la société BELLETABLE, 37 à temps complet, 3 à mi-temps. Parfois, pour faire face à la charge
supplémentaire, la société BELLETABLE fait appel à un ou plusieurs intérimaires.</p>

	<p>Depuis quelques mois, BELLETABLE propose également un service complet avec la location
incluant la livraison, la mise en place des tables, l’installation des divers matériels, le service
à table, lavage de la vaisselle et du linge. Aucune autre société ne propose un service aussi
complet et haut de gamme avec des articles de la table de luxe.</p>

